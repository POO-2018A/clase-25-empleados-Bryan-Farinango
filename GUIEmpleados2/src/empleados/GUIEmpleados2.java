    private void buttonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegistrarActionPerformed
        // TODO add your handling code here:
       
        
        String nombre = textNombre.getText();
        String apellido = textApellido.getText();
        String cargoSeleccionado = (String) comboBoxCargo.getSelectedItem();
        
        Empleado trabajador = new Empleado(nombre,apellido,cargoSeleccionado );
        listaEmpelados.add(trabajador);
        
        contador++;
        String contadorToString = String.valueOf(contador);
        
        modeloTabla.addRow(new String[]{contadorToString ,nombre,apellido,cargoSeleccionado} );
        tableEmpleado.setModel(modeloTabla);
        
    }//GEN-LAST:event_buttonRegistrarActionPerformed

    private void buttonFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFiltrarActionPerformed
        // TODO add your handling code here:
        
        String cargoFiltrado = (String) comboBoxRegistro.getSelectedItem();
        
        System.out.println("Cargo filtrado:"+cargoFiltrado);
     
        for(Empleado item: listaEmpelados){
            
            if(cargoFiltrado.equals("Gerente")){
            
                if(item.getCargo().equals("Gerente")){
                                  modeloTabla2.addRow(new String[]{item.getApellido() ,item.getNombre(), item.getCargo()} );

                }
                
            }
            
            if(cargoFiltrado.equals("Secretaria")){
            
                  modeloTabla2.addRow(new String[]{item.getApellido() ,item.getNombre(), item.getCargo()} );
                
            }
        
        }
        
        
        
        
    }//GEN-LAST:event_buttonFiltrarActionPerformed

